package br.com.pooCorrecao;

public class Aviao {

	//Atributos --> Variaveis
	
	private String lugarOcupado;
	private String capacidadeTotal;
	private String classe ;
	private String empresa;
	public String getLugarOcupado() {
		return lugarOcupado;
	}
	public void setLugarOcupado(String lugarOcupado) {
		this.lugarOcupado = lugarOcupado;
	}
	public String getCapacidadeTotal() {
		return capacidadeTotal;
	}
	public void setCapacidadeTotal(String capacidadeTotal) {
		this.capacidadeTotal = capacidadeTotal;
	}
	public String getClasse() {
		return classe;
	}
	public void setClasse(String classe) {
		this.classe = classe;
	}
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
}
